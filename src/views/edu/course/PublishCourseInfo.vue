      this.$emit("previous")
    },
    getPublishCourseInfo(){
      publishCourse.getPublishCourseInfo(this.courseId).then(resp=>{
        this.coursePublish=resp.data.courseInfo
      })
    }
  },
  props:["courseId"]
}
</script>

